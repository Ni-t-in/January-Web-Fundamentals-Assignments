var count1 = document.getElementById('counter1');      //accesing the counter using getElementById 
var newcount1 = parseInt(count1.innerText);            //converting the string into integer

function like1(){                                      //created a function for like button
    newcount1+=1;                                      //on clicking the funtion will invoke and increase the value stored in newcount variable by one
    count1.innerText = newcount1;                      //now changing the data of counter to show the increased value
}
function dislike1(){                                    //created a function for dislike button
    newcount1-=1;                                       //on clicking the funtion will invoke and decrease the value stored in newcount variable by one
    count1.innerText = newcount1;                       //now changing the data of counter to show the decreased value
}

